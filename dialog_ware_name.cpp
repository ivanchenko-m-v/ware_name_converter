#include <QHBoxLayout>
#include <QVBoxLayout>

#include "dialog_ware_name.h"
#include "line_edit_ware_name.h"

namespace rele_auto
{

/// ############################################################################
///			class dialog_ware_name
/// ############################################################################

    /// ========================================================================
    ///		CONSTRUCTORS/DESTRUCTOR
    /// ========================================================================

    /// ------------------------------------------------------------------------
	///	dialog_ware_name( )
    /// ------------------------------------------------------------------------
	dialog_ware_name::dialog_ware_name( QWidget *parent /*= 0*/  )
        : QDialog( parent )
	{
        this->initialize( );
	}

    /// ------------------------------------------------------------------------
	///	~dialog_ware_name( )
    /// ------------------------------------------------------------------------
	dialog_ware_name::~dialog_ware_name( )
	{
	}

    /// ========================================================================
    ///		PROPERTIES
    /// ========================================================================

    /// ========================================================================
    ///		OPERATORS
    /// ========================================================================

    /// ========================================================================
    ///		FUNCTIONS
    /// ========================================================================
    /// ------------------------------------------------------------------------
	///	initialize( )
    /// ------------------------------------------------------------------------
    void dialog_ware_name::initialize( )
    {
        QVBoxLayout *layout = new QVBoxLayout;
        layout->addWidget( this->init_line( ) );
        layout->addStretch( 1000 );

        this->setLayout( layout );
    }

    /// ------------------------------------------------------------------------
	///	init_line( )
    /// ------------------------------------------------------------------------
    QWidget* dialog_ware_name::init_line( )
    {
        this->_txt_ware_name = new line_edit_ware_name(this);

        QHBoxLayout *layout = new QHBoxLayout;
        layout->addWidget(this->_txt_ware_name);

        QWidget *w = new QWidget(this);
        w->setLayout(layout);

        return w;
    }

/// ############################################################################
}//namespace rele_auto
